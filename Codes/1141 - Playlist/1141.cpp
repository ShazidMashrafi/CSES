void solve()
{
    int n;
    cin >> n;
    vector<int>songs(n);
    for(int i = 0; i < n; ++i) {
        cin >> songs[i];
    }
    map<int, int> pos;
    int l = 0, len = 0;
    for(int r = 0; r < n; ++r) {
        int song  = songs[r];
        if(pos.find(song) != pos.end() && pos[song] >= l) {
            l = pos[song] + 1;
        }

        pos[song] = r;
        len = max(len, r - l + 1);
    }
    cout << len << endl;
}

